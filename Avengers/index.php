	     	<h2>HOME</h2>
	     	<p style="font-family: Arial; font-weight: normal;" >The Avengers are a fictional team of superheroes appearing in American comic books published by Marvel Comics. The team made its debut in The Avengers (Sept.1963), created by writer-editor Stan Lee and artist/co-plotter Jack Kirby. The Avengers is Lee and Kirby's renovation of a previous superhero team, All-Winners Squad, who appeared in comic books series published by Marvel Comics' predecessor Timely Comics. 
Labeled "Earth's Mightiest Heroes", the Avengers originally consisted of Ant-Man, the Hulk, Iron Man, Thor, and the Wasp. Ant-Man had become Giant-Man by issue #2.<br> The original Captain America was discovered trapped in ice in issue #4, and joined the group after they revived him.A rotating roster became a hallmark of the series, although one theme remained consistent: the Avengers fight "the foes no single superhero can withstand." The team, famous for its battle cry of "Avengers Assemble!", has featured humans, mutants, Inhumans, androids, aliens, supernatural beings, and even former villains. <br>
The team has appeared in a wide variety of media outside of comic books, including a number of different animated television series and direct-to-video films. The 2012 live-action feature film The Avengers, directed by Joss Whedon, set numerous records during its box office run, including one of the biggest opening debuts in North America, with a weekend gross of $207.4 million.<br>A second Avengers film titled Avengers: Age of Ultron was released on May 1, 2015. The Avengers also appeared in Captain America’s third sequel Captain America: Civil War which was released on May 6, 2016. It was followed by Avengers: Infinity War, which became the first superhero film to gross over $2 billion and was released on April 27, 2018. A fourth film, Avengers: Endgame, released on April 26, 2019. 
</p>
